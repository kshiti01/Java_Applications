    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if (userNameTextfield.getText().isEmpty() || passwordTextfield.getText().isEmpty() || restaurantNameTextfield.getText().isEmpty() || restaurantAddressTextfield.getText().isEmpty()
              || restaurantPhoneTextfield.getText().isEmpty()  ) {
            JOptionPane.showMessageDialog(null, "please enter all mandatory fields");
            return;
        }

        if (ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(userNameTextfield.getText())) {
            Restaurant restaurant = new Restaurant();
            restaurant.setRestaurantName(restaurantNameTextfield.getText());
            restaurant.setUsername(userNameTextfield.getText());
            restaurant.setPassword(passwordTextfield.getText());
            restaurant.setRole(new AdminRole());
            restaurant.setRestaurantAddress(restaurantAddressTextfield.getText());
            restaurant.setRestaurantphone(restaurantPhoneTextfield.getText());
            ecosystem.getUserAccountDirectory().addUserAccount(restaurant);
            ecosystem.getRestaurantDirectory().addRestaurant(restaurant);
            populateTable();
            userNameTextfield.setText("");
            passwordTextfield.setText("");
            restaurantNameTextfield.setText("");
            restaurantAddressTextfield.setText("");
            restaurantPhoneTextfield.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "Username " + userNameTextfield.getText() + " already exists !!!, Please try a new one");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

