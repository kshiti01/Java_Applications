    private void populateTable() {
        RestaurantDirectory restaurantDirectory = ecosystem.getRestaurantDirectory();
        DefaultTableModel model = (DefaultTableModel) tblRestaurantAdmin.getModel();
        model.setRowCount(0);
        for (Restaurant restaurant : restaurantDirectory.getRestaurantDirectory()) {
            Object[] row = new Object[5];
            row[0] = restaurant;
            row[1] = restaurant.getRestaurantAddress();
            row[2] = restaurant.getRestaurantphone();
            row[3] = restaurant.getUsername();
            row[4] = restaurant.getPassword();
            model.addRow(row);
        }
    }

   
    private void initListners() {
       tblRestaurantAdmin.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
        public void valueChanged(ListSelectionEvent event) {
           int selectedRow = tblRestaurantAdmin.getSelectedRow();
             if (selectedRow >= 0) {
                  Restaurant  restaurant  = (Restaurant) tblRestaurantAdmin.getValueAt(selectedRow, 0);
                 if(restaurant!=null){
                     display(restaurant);
                 }
             }
        }
    });
    }

    private void display(Restaurant restaurant) {
        userNameTextfield.setText(restaurant.getUsername());
        passwordTextfield.setText(restaurant.getPassword());
        restaurantNameTextfield.setText(restaurant.getRestaurantName());
        restaurantAddressTextfield.setText(restaurant.getRestaurantAddress());
        restaurantPhoneTextfield.setText(restaurant.getRestaurantphone());
        

    }

